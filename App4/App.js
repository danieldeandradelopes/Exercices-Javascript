// comparações de strings
// operadores de comparação == e !=

"a" == "b"; // maneira incorreta de comparar igualdade
"a" == "b";// maneira correta de comparar igualdade -- A NÃO É IGUAL B - RETORNARÁ | false = 0 | true = 1
"a" == "a"; // retornará com true



//--------------------------------------------------------------
//O JavaScript é casesensitive --- "A" não é igual "a"



"A" == "a"; //false
"A" == "B"; //false
"b" == "b"; //true
"3" == "3"; //true
"3" == "três" //false

//--------------------------------------------------------------

//!= quer dizer 'DIFERENTE'

"a" != "a"; //false
"a" != "b"; //true



//--------------------------------------------------------------

"A" != "a"; //true
"B" != "b"; //true
"3" != "3"; //false
"b" != "b"; //false
"3" != "três";//true




